package Exercicios;

public class aula30e01 {
	public static void main(String[] args) {	
		//Entrada 10,30 	; 	Saida SOMA = 40
		int varA = 10;
		int varB = 30;
		System.out.printf("SOMA = %d%n",(varA+varB));
		
		//Entrada -30,10 	;	Saída SOMA = -20
		int varC = -30;
		int varD = 10;
		System.out.printf("SOMA = %d%n",(varC+varD));
		
		//Entrada 0,0		;	Saída SOMA = 0	
		int varE,varF;
		varE=varF=0;
		System.out.printf("SOMA = %d%n",(varE+varF));
	}
}
